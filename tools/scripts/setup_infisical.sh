    # Add the infisical dependency if not present
    if ! grep -q "infisical" Cargo.toml; then
        print_warning "Adding infisical dependency to Cargo.toml..."
        cat >> Cargo.toml << 'EOF'

[dependencies.infisical]
infisical = "0.4"
